#include <stdio.h>

int main() {
  int a = 10;

  if (a == 10) {
    printf("A es igual a 10 \n");
  } else if (a == 20) {
    printf("A es igual a 20 \n");
  } else if (a == 30) {
    printf("A es igual a 30\n");
  } else {
    printf("Los valores ingresados no coinciden con el valor de A.");
  }
}